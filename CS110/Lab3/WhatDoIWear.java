   public class WhatDoIWear
   {
      public static void main(String[] args)
      {
         //temperature variable
         double tempOutsideF = 71;
         
         // if temp is less than 30
         if(tempOutsideF < 30)
         {
            //output this clothing option
            System.out.println("Wear boots, sweater, and gloves.");
         }
         
         //if temp is more than 60
         if(tempOutsideF > 60)
         {
            //output this clothing option
            System.out.println("Wear a t-shirt, shorts and sandals.");
         }
         
      }
      
      
   }

















